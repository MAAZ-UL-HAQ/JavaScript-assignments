function isLeap(year){
    if(year%4==0){
        if(year%100){
            if(year%400){
                alert(year + " is a leap year with 366 days");
            }
            else{
                alert(year + " is NOT a leap year and has 365 days");
            }
        }
        else{
            alert(year + " is a leap year with 366 days");
        }
    }
    else{
        alert(year + " is NOT a leap year and has 365 days");
    }
}

var year = +prompt("Enter year to find whether year is leap year");
isLeap(year);

